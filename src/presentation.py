import streamlit as st
import reveal_slides as rs

def presentation_page():
    st.title("Презентация проекта")
    presentation_markdown = """
    # Прогнозирование отказов оборудования
    Цель: Разработать модель для предсказания отказов оборудования.
    ---
    ## Введение
    - Датасет: AI4I 2020 Predictive Maintenance Dataset (10 000 записей, 14 признаков).
    - Цель: Предсказать отказ оборудования (Machine failure = 1) или его отсутствие (Machine failure = 0).
    ---
    ## Этапы работы
    1. Загрузка данных (CSV или ucimlrepo).
    2. Предобработка: удаление ненужных столбцов, кодирование категориальных переменных, масштабирование.
    3. Обучение модели (Logistic Regression).
    4. Оценка: Accuracy, Confusion Matrix, ROC-AUC.
    5. Визуализация результатов.
    ---
    ## Streamlit-приложение
    - **Основная страница**: Загрузка данных, обучение модели, предсказания.
    - **Презентация**: Описание проекта в формате слайдов.
    ---
    ## Заключение
    - Итоги: Модель успешно предсказывает отказы.
    - Возможные улучшения: Использование других алгоритмов (Random Forest, XGBoost), настройка гиперпараметров.
    """
    rs.slides(
        presentation_markdown,
        height=500,
        theme="serif",
        config={
            "transition": "slide",
            "controls": True,  # Включаем элементы управления
            "progress": True,  # Показываем прогресс-бар
            "slideNumber": True,  # Показываем номера слайдов
            "history": True,  # Включаем навигацию через историю браузера
        }
    )